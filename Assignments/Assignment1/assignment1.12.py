# Membership - in operator
numbers = [1, 2, 3, 4, 5]
x = 3
print("in operator:", x in numbers)  # Output: True

# Membership - not in operator
numbers = [1, 2, 3, 4, 5]
x = 6
print("not in operator:", x not in numbers)  # Output: True

# Membership - in operator with strings
fruits = ["apple", "banana", "orange"]
fruit = "banana"
print("in operator with strings:", fruit in fruits)  # Output: True

# Membership - not in operator with strings
fruits = ["apple", "banana", "orange"]
fruit = "grape"
print("not in operator with strings:", fruit not in fruits)  # Output: True
