# Equal to
x = 5
y = 5
print("Equal to:", x == y)  # Output: True

# Not equal to
x = 5
y = 3
print("Not equal to:", x != y)  # Output: True

# Greater than
x = 7
y = 5
print("Greater than:", x > y)  # Output: True

# Less than
x = 3
y = 5
print("Less than:", x < y)  # Output: True

# Greater than or equal to
x = 5
y = 5
print("Greater than or equal to:", x >= y)  # Output: True

# Less than or equal to
x = 3
y = 5
print("Less than or equal to:", x <= y)  # Output: True
